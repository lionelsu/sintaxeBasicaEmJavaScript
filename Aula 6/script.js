// tipos de função
// declarativas
function funcao() {
    console.log('Sou uma mensagem de uma funcao declarativa')
}

funcao();

// expressão de função
var funcao = function funcao() {
    console.log('Sou uma mensagem de função de expressão!')
}

funcao();

// arrow funtion
var funcao = () => {
    console.log('Sou uma arrow function');
}
funcao();